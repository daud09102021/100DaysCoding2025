public class Day25 {
    public static void main(String[] args) {
        // Operator Increment dan Decrement (++, --)
        int a = 10;
        // increment
        a++; 
        System.out.println("Nilai a setelah di increment : " + a);
        // decrement
        a--; 
        System.out.println("Nilai a setelah di decrement : " + a);
        
    }
}
